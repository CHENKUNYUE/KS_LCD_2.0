import sys

def make_icon_array(name, ascii_art):
    bytes_list = []
    for page in range(2):
        for col in range(16):
            byte_val = 0
            for row in range(8):
                y = page * 8 + row
                if y < len(ascii_art) and col < len(ascii_art[y]):
                    char = ascii_art[y][col]
                    if char == '#':
                        byte_val |= (1 << row)
            bytes_list.append(byte_val)
            
    header = f"const uint8_t Icon_{name}_16x16[32] = {{"
    body = ", ".join([f"0x{b:02X}" for b in bytes_list])
    footer = "};\n"
    return header + "\n    " + body + "\n" + footer

