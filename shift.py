out = []
for i in range(64):
    val = (data[i] & 0xFF) | ((data[i+64] & 0xFF) << 8) | ((data[i+128] & 0xFF) << 16)
    # We want to shift UP by 4 pixels. In the byte, LSB is top. So we shift towards the LSB!
    # Which means we shift RIGHT by 4.
    val = val >> 4
    out.append(val & 0xFF)
    out.append((val >> 8) & 0xFF)
    out.append((val >> 16) & 0xFF)

p0 = out[0::3]
p1 = out[1::3]
p2 = out[2::3]

with open("shifted.c", "w") as f:
    for p in [p0, p1, p2]:
        hexs = [f"0x{x:02X}" for x in p]
        for idx in range(0, 64, 16):
            f.write("    " + ", ".join(hexs[idx:idx+16]) + ",\n")
        f.write("\n")
